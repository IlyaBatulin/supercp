from __future__ import annotations

from pathlib import Path

from docx import Document
from docx.shared import Pt


BASE = Path(__file__).resolve().parent.parent


def set_normal_style(doc: Document) -> None:
    style = doc.styles["Normal"]
    style.font.name = "Times New Roman"
    style.font.size = Pt(14)


def add_paragraphs(doc: Document, text: str) -> None:
    for block in text.strip().split("\n\n"):
        paragraph = doc.add_paragraph(block.strip())
        paragraph.paragraph_format.first_line_indent = Pt(28)


def add_code(doc: Document, title: str, code: str) -> None:
    doc.add_paragraph(title)
    paragraph = doc.add_paragraph()
    run = paragraph.add_run(code.rstrip())
    run.font.name = "Courier New"
    run.font.size = Pt(10)


def read_file(relative_path: str) -> str:
    return (BASE / relative_path).read_text(encoding="utf-8")


def excerpt(text: str, start_marker: str, end_marker: str | None = None) -> str:
    start = text.index(start_marker)
    if end_marker is None:
        return text[start:]
    end = text.index(end_marker, start)
    return text[start:end]


